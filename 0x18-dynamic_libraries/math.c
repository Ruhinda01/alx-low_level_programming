#include <stdio.h>
/**
 * add - adds two integers
 * @a: number 1
 * @b: number 2
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two numbers
 * @a: number 1
 * @b: number 2
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply two numbers
 * @a: number 1
 * @b: number 2
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide two numbers
 * @a: number 1
 * @b: number 2
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus of two numbers
 * @a: number 1
 * @b: number 2
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
